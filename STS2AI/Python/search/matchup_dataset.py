#!/usr/bin/env python3
"""Dataset loader for offline card ranking data.

Loads JSONL + NPZ data generated by generate_card_ranking_data.py for
mixed-batch training with PPO. Provides pre-encoded state/action tensors
and target ranking scores.

Usage:
    dataset = MatchupRankingDataset("datasets/card_ranking_v1/")
    batch = dataset.sample_batch(32, device="cuda")
    # batch["state_tensors"], batch["action_tensors"], batch["target_scores"], batch["option_mask"]
"""
from __future__ import annotations

import json
import random
from pathlib import Path
from typing import Any

import numpy as np
import torch


MAX_OPTIONS = 4  # card A, B, C, skip


class MatchupRankingDataset:
    """Loads offline card ranking data for mixed-batch training."""

    def __init__(
        self,
        data_dir: str | Path,
        max_samples: int | None = None,
        min_spread: float = 0.0,
    ) -> None:
        self.data_dir = Path(data_dir)
        self.samples: list[dict[str, Any]] = []
        self.tensors_cache: dict[str, dict[str, np.ndarray]] = {}
        self._min_spread = min_spread
        self._filtered_count = 0

        self._load_data(max_samples)

    def _load_data(self, max_samples: int | None = None) -> None:
        """Load JSONL data files from directory."""
        jsonl_files = sorted(self.data_dir.glob("*.jsonl"))
        if not jsonl_files:
            derived_ranking = self.data_dir / "derived" / "rl" / "ranking_sample.jsonl"
            if derived_ranking.exists():
                jsonl_files = [derived_ranking]
        if not jsonl_files:
            return

        for jsonl_path in jsonl_files:
            with open(jsonl_path, "r", encoding="utf-8") as f:
                for line in f:
                    line = line.strip()
                    if not line:
                        continue
                    sample = json.loads(line)
                    # Validate: must have scores and at least 2 options
                    scores = sample.get("scores") or []
                    if len(scores) < 2:
                        continue
                    # Filter out zero-spread samples (no ranking signal)
                    spread = max(scores) - min(scores)
                    if spread < self._min_spread:
                        self._filtered_count += 1
                        continue
                    self.samples.append(sample)

                    if max_samples and len(self.samples) >= max_samples:
                        return

    def _load_tensors(self, tensor_path: str) -> dict[str, np.ndarray]:
        """Load pre-encoded tensors from NPZ file (with caching)."""
        if tensor_path not in self.tensors_cache:
            full_path = self.data_dir / tensor_path
            if full_path.exists():
                self.tensors_cache[tensor_path] = dict(np.load(str(full_path), allow_pickle=True))
            else:
                self.tensors_cache[tensor_path] = {}
        return self.tensors_cache[tensor_path]

    def __len__(self) -> int:
        return len(self.samples)

    def sample_batch(
        self,
        batch_size: int,
        device: str | torch.device = "cpu",
    ) -> dict[str, Any] | None:
        """Sample a random batch of ranking data.

        Returns:
            Dict with:
                "target_scores": (B, MAX_OPTIONS) float tensor
                "option_mask": (B, MAX_OPTIONS) bool tensor
                "state_tensors": dict of (B, ...) tensors (if NPZ available)
                "action_tensors": dict of (B, ...) tensors (if NPZ available)
                "metadata": list of sample dicts
            Or None if no data available.
        """
        if not self.samples:
            return None

        indices = random.choices(range(len(self.samples)), k=batch_size)
        batch_samples = [self.samples[i] for i in indices]

        # Build target scores and mask
        target_scores = np.zeros((batch_size, MAX_OPTIONS), dtype=np.float32)
        option_mask = np.zeros((batch_size, MAX_OPTIONS), dtype=bool)

        for b, sample in enumerate(batch_samples):
            scores = sample["scores"]
            n = min(len(scores), MAX_OPTIONS)
            target_scores[b, :n] = scores[:n]
            option_mask[b, :n] = True

        result: dict[str, Any] = {
            "target_scores": torch.tensor(target_scores, dtype=torch.float32, device=device),
            "option_mask": torch.tensor(option_mask, dtype=torch.bool, device=device),
            "metadata": batch_samples,
        }

        # Try to load pre-encoded state/action tensors
        state_tensor_batches: dict[str, list[np.ndarray]] = {}
        action_tensor_batches: dict[str, list[np.ndarray]] = {}
        has_tensors = True

        for sample in batch_samples:
            tensor_path = sample.get("state_tensors_path")
            if not tensor_path:
                has_tensors = False
                break
            tensors = self._load_tensors(tensor_path)
            if not tensors:
                has_tensors = False
                break

            for key, arr in tensors.items():
                if key.startswith("state_"):
                    clean_key = key[len("state_"):]
                    state_tensor_batches.setdefault(clean_key, []).append(arr)
                elif key.startswith("action_"):
                    clean_key = key[len("action_"):]
                    action_tensor_batches.setdefault(clean_key, []).append(arr)

        if has_tensors and state_tensor_batches:
            result["state_tensors"] = {}
            for k, v in state_tensor_batches.items():
                stacked = np.stack(v)
                # Ensure correct dtypes for the network
                if stacked.dtype == np.bool_:
                    result["state_tensors"][k] = torch.tensor(stacked, dtype=torch.bool, device=device)
                elif stacked.dtype in (np.int64, np.int32):
                    result["state_tensors"][k] = torch.tensor(stacked, dtype=torch.long, device=device)
                else:
                    result["state_tensors"][k] = torch.tensor(stacked, dtype=torch.float32, device=device)
            result["action_tensors"] = {}
            for k, v in action_tensor_batches.items():
                stacked = np.stack(v)
                if stacked.dtype == np.bool_:
                    result["action_tensors"][k] = torch.tensor(stacked, dtype=torch.bool, device=device)
                elif stacked.dtype in (np.int64, np.int32):
                    result["action_tensors"][k] = torch.tensor(stacked, dtype=torch.long, device=device)
                else:
                    result["action_tensors"][k] = torch.tensor(stacked, dtype=torch.float32, device=device)

        return result

    def get_stats(self) -> dict[str, Any]:
        """Return dataset statistics."""
        if not self.samples:
            return {"num_samples": 0, "filtered_out": self._filtered_count}

        all_scores = [s["scores"] for s in self.samples]
        num_options = [len(s) for s in all_scores]
        best_indices = [s.get("best_idx", 0) for s in self.samples]

        return {
            "num_samples": len(self.samples),
            "avg_options": np.mean(num_options),
            "score_mean": np.mean([np.mean(s) for s in all_scores]),
            "score_std": np.mean([np.std(s) for s in all_scores if len(s) > 1]),
            "skip_best_rate": sum(1 for s in self.samples
                                  if s.get("best_idx") == len(s.get("scores", [])) - 1)
                             / max(1, len(self.samples)),
        }
